package smokeTesting;

public class Playground {
    public static void main (String[] args) {
        int a = 4;
        for (int i = 0; i < a; i ++) {
            int orderOfNumber = i;

            if (orderOfNumber == 1) {
                System.out.println(10);
            };

            if (orderOfNumber == 2) {
                System.out.println(20);
            }

            if (orderOfNumber == 3) {
                System.out.println(30);
            }
        }
    }
}
